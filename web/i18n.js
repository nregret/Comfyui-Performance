function getLanguage() {
  const setting = app?.ui?.settings?.getSettingValue?.("Comfy.Locale");
  const locale = String(setting || navigator.language || "en").toLowerCase();
  return locale.includes("zh") ? "zh" : "en";
}

export function t(key, values = {}) {
  let text = TRANSLATIONS[getLanguage()]?.[key] || TRANSLATIONS.en[key] || key;
  for (const [name, value] of Object.entries(values)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}
